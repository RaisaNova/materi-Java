package tugas3;

public class gerbang {
    private String nama;
    private String lokasi;

    public gerbang(String nama, String lokasi) {
        this.nama = nama;
        this.lokasi = lokasi;
    }

    public String getNama() {
        return nama;
    }

    public String setNama(String nama) {
        return nama;
    }

    public String getLokasi() {
        return lokasi;
    }

    public String setLokasi(String Lokasi) {
        return lokasi;
    }
}
